    private void btn_themActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_themActionPerformed
        // TODO add your handling code here:
        if(txt_maKH.getText().isBlank() || txt_tenKH.getText().isBlank() || txt_sdt.getText().isBlank() || txt_diaChi.getText().isBlank() ){
            JOptionPane.showMessageDialog(this, "Không được để trống!");
            return;
        }
        if(!_check.checkSDT(txt_sdt.getText()) || !_check.checkSo(txt_sdt.getText())){
            JOptionPane.showMessageDialog(this,"Số điện thoại phải đúng 10 kí tự là số!");
            return;
        }
        
        int input = JOptionPane.showConfirmDialog(null, "Bạn xác định muốn thêm?", "Selection an Option", JOptionPane.YES_NO_OPTION);
        if(input == 0){
            JOptionPane.showMessageDialog(this, _qlALL.createKH(getGUI()));
            fillTable(_qlALL.getKhachHang());
        }
        
        //JOptionPane.showMessageDialog(this, "Thêm thành công");
    }//GEN-LAST:event_btn_themActionPerformed


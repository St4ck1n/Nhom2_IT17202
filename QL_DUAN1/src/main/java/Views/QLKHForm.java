    private void btn_timKiemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_timKiemActionPerformed
        // TODO add your handling code here:
        if(txt_timKiem.getText().isBlank()){
            JOptionPane.showMessageDialog(this, "Không đc để trống");
            return;
        }
        if(!_check.checkSDT(txt_timKiem.getText()) || !_check.checkSo(txt_timKiem.getText())){
            JOptionPane.showMessageDialog(this,"Số điện thoại phải đúng 10 kí tự là số!");
            return;
        }
        int input = Integer.parseInt(String.valueOf(_qlALL.findBySdt(txt_timKiem.getText()).size()));
        if(input == 0){
            JOptionPane.showMessageDialog(this, "Không tìm thấy khách hàng cần tìm!");
            fillTable(_qlALL.findBySdt(txt_timKiem.getText()));
            return;   
        }
        fillTable(_qlALL.findBySdt(txt_timKiem.getText()));
    }//GEN-LAST:event_btn_timKiemActionPerformed

